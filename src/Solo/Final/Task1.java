package Solo.Final;
/*ключевое слово final используется для присвоения значения только один раз
* Методы и классы тоже могут быть помечены final. Для ограничения методов,
* чтобы они не могли быть переопределены, и классов, чтобы они не могли быть подклассифицируемы */
public class Task1 {
    public static final double PI=3.14;
    public static void main(String[] args) {
        System.out.println(PI);
    }
}
